"""These are the Operation classes"""


class Addition:
    """This is the Addition Class"""

    @staticmethod
    def add(value_1, value_2):
        return value_1 + value_2


class Subtraction:
    """This is the Addition Class"""

    @staticmethod
    def subtract(value_1, value_2):
        return value_1 - value_2


class Multiplication:
    """This is the Addition Class"""

    @staticmethod
    def multiply(value_1, value_2):
        return value_1 * value_2
